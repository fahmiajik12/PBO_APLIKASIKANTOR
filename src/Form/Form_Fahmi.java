    public void loadData(){
        DefaultTableModel models=new DefaultTableModel();
        models.addColumn("ID");
        models.addColumn("NPM");
        models.addColumn("Nama Depan");
        models.addColumn("Nama Tengah");
        models.addColumn("Nama Belakang");
        models.addColumn("No HP");
        

        tabel.setModel(models);
            try {
                Statement statement=(Statement)Connect.GetConnection().createStatement();
                ResultSet res=statement.executeQuery("select * from fahmi");
                while(res.next())
                {
                models.addRow(new Object[]{
                    res.getString("id"),
                    res.getString("npm"),
                    res.getString("nama_depan"),
                    res.getString("nama_tengah"),
                    res.getString("nama_belakang"),
                    res.getString("no_hp"),
                });
                tabel.setModel(models);
                }
        }catch (Exception e){
            JOptionPane.showMessageDialog(rootPane,"Tidak Ada Data");
        }
        }

