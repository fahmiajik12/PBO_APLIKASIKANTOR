    private void simpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_simpanActionPerformed
        // TODO add your handling code here:
        String nim = npm.getText();
        String nama = nama_depan.getText();
        String nama2 = nama_tengah.getText();
        String nama3 = nama_belakang.getText();
        String nomor = no_hp.getText();
        String id = (String)tabel.getValueAt(row, 0);
        if (addNew == true) {
            if("".equals(nim) || "".equals(nama) || "".equals(nama3) || "".equals(nomor)){
                JOptionPane.showMessageDialog(null, "Harap Isi Semua Data!");
            } else{
                try{
                    Statement stm = (Statement) Connect.GetConnection().createStatement();
                    stm.execute("INSERT into fahmi VALUES (null,'" + nim + "','" + nama + "','" + nama2 + "','" + nama3 + "','" + nomor + "');");
                    stm.close();
                    JOptionPane.showMessageDialog(null, "Data Berhasil Disimpan!","SIMPAN "
                            + "DATA",JOptionPane.INFORMATION_MESSAGE);
                }
                catch (Exception t){
                    JOptionPane.showMessageDialog(null, "Data Gagal Disimpan!","SIMPAN "
                            + "DATA",JOptionPane.INFORMATION_MESSAGE);
                }
                loadData();
            }
        }else{
            try{
            Statement stm = (Statement) Connect.GetConnection().createStatement();
            stm.execute("UPDATE fahmi SET " + "npm = '" + nim + "'," + "nama_depan = '" + nama + "'," + "nama_tengah = '" + nama2 + "'," + "nama_belakang = '" + nama3 + "',"+ "no_hp = '" + nomor + "' " + "WHERE id = " + id + ";");
            stm.close();
                JOptionPane.showMessageDialog(null, "Data Berhasil Disimpan!","SIMPAN "
                        + "DATA",JOptionPane.INFORMATION_MESSAGE);
            }
            catch (Exception t){
                JOptionPane.showMessageDialog(null, "Data Gagal Disimpan!","SIMPAN "
                        + "DATA",JOptionPane.INFORMATION_MESSAGE);
            }
            loadData();
        }
    }//GEN-LAST:event_simpanActionPerformed

    private void hapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_hapusActionPerformed
        // TODO add your handling code here:
        String id = (String)tabel.getValueAt(row, 0);

        int Pilih = JOptionPane.showConfirmDialog(null,"Anda Yakin Data Akan Dihapus??","HAPUS "
                + "DATA",JOptionPane.YES_NO_OPTION,JOptionPane.WARNING_MESSAGE);
        if(Pilih == JOptionPane.YES_OPTION){
            try{
                Statement stm = (Statement) Connect.GetConnection().createStatement();
                stm.execute("DELETE from fahmi WHERE id = '" + id + "';");
                stm.close();
                JOptionPane.showMessageDialog(null, "Data Berhasil Dihapus!");
            }
            catch (Exception t){
                JOptionPane.showMessageDialog(null, "Data Gagal Dihapus!");
            }
            loadData();
            row = 0;
        }
        else if(Pilih == JOptionPane.NO_OPTION){
        }
    }//GEN-LAST:event_hapusActionPerformed

