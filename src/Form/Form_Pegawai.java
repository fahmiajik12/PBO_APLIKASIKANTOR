    private void simpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_simpanActionPerformed
        // TODO add your handling code here:
        String nama = nama_pegawai.getText();
        String jenis = (String)gender.getSelectedItem();
        String jabatan = jabatan_pegawai.getText();
        String kode = (String)tabel1.getValueAt(row, 0);
        if (addNew == true) {
            if("".equals(nama) || "".equals(jenis) || "".equals(jabatan)){
                JOptionPane.showMessageDialog(null, "Harap Isi Semua Data!");
            } else{
                try{
                    Statement stm = (Statement) Connect.GetConnection().createStatement();
                    stm.execute("INSERT into pegawai VALUES (null,'" + nama + "','" + jenis + "','" + jabatan + "');");
                    stm.close();
                    JOptionPane.showMessageDialog(null, "Data Berhasil Disimpan!","SIMPAN DATA",JOptionPane.INFORMATION_MESSAGE);
                } catch (Exception t){
                    JOptionPane.showMessageDialog(null, "Data Gagal Disimpan!","SIMPAN DATA",JOptionPane.INFORMATION_MESSAGE);
                }
                loadData();
                }
            }else{
                try{
                    Statement stm = (Statement) Connect.GetConnection().createStatement();
                    stm.execute("UPDATE pegawai SET " + "nama_pegawai = '" + nama + "'," + "gender_pegawai = '" + jenis + "'," + "jabatan_pegawai = '" + jabatan + "' " + "WHERE kode_pegawai = " + kode + ";");
                    stm.close();
                    JOptionPane.showMessageDialog(null, "Data Berhasil Disimpan!","SIMPAN DATA",JOptionPane.INFORMATION_MESSAGE);
                } catch (Exception t){
                    JOptionPane.showMessageDialog(null, "Data Gagal Disimpan!","SIMPAN DATA",JOptionPane.INFORMATION_MESSAGE);
                }
                loadData();
            }
       
    }//GEN-LAST:event_simpanActionPerformed

