    public void loadData(){
    DefaultTableModel models=new DefaultTableModel();
    models.addColumn("Kode");
    models.addColumn("Nama Pegawai");
    models.addColumn("Jenis Kelamin");
    models.addColumn("Jabatan");
    
    try {
    Statement statement=(Statement)Connect.GetConnection().createStatement();
    ResultSet res=statement.executeQuery("select * from pegawai");
    while(res.next())
    {
    models.addRow(new Object[]{
       res.getString("kode_pegawai"),
       res.getString("nama_pegawai"),
       res.getString("gender_pegawai"),
       res.getString("jabatan_pegawai"),
    });
    tabel1.setModel(models);
    }
    }catch (Exception e){
        JOptionPane.showMessageDialog(rootPane,"Tidak Ada Data");
    }
 }

