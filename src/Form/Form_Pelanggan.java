    private void simpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_simpanActionPerformed
        // TODO add your handling code here:
        String nama = nama_pelanggan.getText();
        String nomor = nomor_pelanggan.getText();
        String kode = (String)tabel2.getValueAt(row, 0);
        if (addNew == true) {
            if("".equals(nama) || "".equals(nomor)){
                JOptionPane.showMessageDialog(null, "Harap Isi Semua Data!");
            } else{
                try{
                    Statement stm = (Statement) Connect.GetConnection().createStatement();
                    stm.execute("INSERT into pelanggan VALUES (null,'" + nama_pelanggan.getText() + "','" + nomor_pelanggan.getText() + "');");
                    stm.close();
                    JOptionPane.showMessageDialog(null, "Data Berhasil Disimpan!","SIMPAN DATA",JOptionPane.INFORMATION_MESSAGE);
                } catch (Exception t){
                    JOptionPane.showMessageDialog(null, "Data Gagal Disimpan!","SIMPAN DATA",JOptionPane.INFORMATION_MESSAGE);
                }
                loadData();
                }
                }else{
                    try{
                        Statement stm = (Statement) Connect.GetConnection().createStatement();
                        stm.execute("UPDATE pelanggan SET " + "nama_pelanggan = '" + nama + "'," + "nomor_pelanggan = '" + nomor + "' " + "WHERE kode_pelanggan = " + kode + ";");
                        stm.close();
                        JOptionPane.showMessageDialog(null, "Data Berhasil Disimpan!","SIMPAN DATA",JOptionPane.INFORMATION_MESSAGE);
                    } catch (Exception t){
                        JOptionPane.showMessageDialog(null, "Data Gagal Disimpan!","SIMPAN DATA",JOptionPane.INFORMATION_MESSAGE);
                    }
                    loadData();
    }//GEN-LAST:event_simpanActionPerformed
    }
    private void hapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_hapusActionPerformed
        // TODO add your handling code here:
        String kode = (String)tabel2.getValueAt(row, 0);

        int Pilih = JOptionPane.showConfirmDialog(null,"Anda Yakin Data Akan Dihapus??","HAPUS DATA",JOptionPane.YES_NO_OPTION,JOptionPane.WARNING_MESSAGE);
        if(Pilih == JOptionPane.YES_OPTION){
        try{
            Statement stm = (Statement) Connect.GetConnection().createStatement();
            stm.execute("DELETE from pelanggan WHERE kode_pelanggan = '" + kode + "';");
            stm.close();
            JOptionPane.showMessageDialog(null, "Data Berhasil Dihapus!");
        } catch (Exception t){
            JOptionPane.showMessageDialog(null, "Data Gagal Dihapus!");
        }
        loadData();
        row = 0;
        } else if(Pilih == JOptionPane.NO_OPTION){
        }
    }//GEN-LAST:event_hapusActionPerformed

