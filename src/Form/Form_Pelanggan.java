    public void loadData(){
    DefaultTableModel models=new DefaultTableModel();
    models.addColumn("Kode");
    models.addColumn("Nama Pelanggan");
    models.addColumn("Nomor HP");

    
    try {
        Statement statement=(Statement)Connect.GetConnection().createStatement();
        ResultSet res=statement.executeQuery("select * from pelanggan");
        while(res.next())
    {
       models.addRow(new Object[]{
        res.getString("kode_pelanggan"),
        res.getString("nama_pelanggan"),
        res.getString("nomor_pelanggan"),
        });
       tabel2.setModel(models);
        }
        }catch (Exception e){
        JOptionPane.showMessageDialog(rootPane,"Tidak Ada Data");
    }
}
